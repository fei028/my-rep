package com.xlkh.platform.pojo.system;

import java.io.Serializable;
import java.util.Date;

/**
 * 系统_用户组_用户
 * @author fei
 *
 */
public class SysGroupUser implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = -3626023916610142218L;
	/** 关系ID */
	private Integer id;
	/** 用户ID */
	private Integer userId;
	/** 用户组 */
	private Integer groupId;
	/** 创建人(id) */
	private Integer createUser;
	/** 创建时间 */
	private Date createTime;
	/** 更新人(id) */
	private Integer updateUser;
	/** 更新时间 */
	private Date updateTime;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getGroupId() {
		return groupId;
	}
	public void setGroupId(Integer groupId) {
		this.groupId = groupId;
	}
	public Integer getCreateUser() {
		return createUser;
	}
	public void setCreateUser(Integer createUser) {
		this.createUser = createUser;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Integer getUpdateUser() {
		return updateUser;
	}
	public void setUpdateUser(Integer updateUser) {
		this.updateUser = updateUser;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	@Override
	public String toString() {
		return "SysGroupUser [id=" + id + ", userId=" + userId + ", groupId=" + groupId + ", createUser=" + createUser
				+ ", createTime=" + createTime + ", updateUser=" + updateUser + ", updateTime=" + updateTime + "]";
	}
	
}
